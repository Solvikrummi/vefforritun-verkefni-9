  const detailsElement = el(
    'div',
    { class: 'launch-details' },
    el('h2', {}, result.name),
    el('p', {}, result.details),
    // B√¶ta vi√∞ fleiri uppl√Ωsingum eftir √æ√∂rfum
  );

  container.appendChild(detailsElement);
  /**
  * Birta ni√∞urst√∂√∞ur √∫r leit.
  * @param {import('./api.types.js').Launch[] | null} results Ni√∞urst√∂√∞ur √∫r leit
  * @param {string} query Leitarstrengur.
  * @param {HTMLElement} parentElement Element sem √° a√∞ birta ni√∞urst√∂√∞ur √≠.
  */
  function createSearchResults(results, query, parentElement) {
    const list = el('ul', { class: 'results' });

    if (!results) {
      const noResultsElement = el('li', {}, `Villa vi√∞ leit a√∞ ${query}`);
      list.appendChild(noResultsElement);
      return list;
    }

    if (results.length === 0) {
      const noResultsElement = el(
        'li',
        {},
        `Engar ni√∞urst√∂√∞ur fyrir leit a√∞ ${query}`
      );
      list.appendChild(noResultsElement);
      return list;
    }

    for (const result of results) {
      const articleElement = el(
        'li',
        { class: 'article' },
        el('h3', {}, result.title), // Assuming each result has a title
        el('p', {}, result.summary), // Assuming each result has a summary
        // Add more details about the article here
      );

      list.appendChild(articleElement);
    }

    return list;
  }


